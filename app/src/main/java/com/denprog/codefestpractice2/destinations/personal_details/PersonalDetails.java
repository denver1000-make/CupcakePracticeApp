package com.denprog.codefestpractice2.destinations.personal_details;

public class PersonalDetails {

    public String firstName;
    public String lastName;
    public String middleName;
    public String cakeMessage;

    public PersonalDetails(String firstName, String lastName, String middleName, String cakeMessage) {
        this.firstName = firstName;
        this.lastName = lastName;
        this.middleName = middleName;
        this.cakeMessage = cakeMessage;
    }
}
